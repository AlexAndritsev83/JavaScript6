// Напишите код, который посчитает сумму всех парных элементов в массиве.
// Использовать встроенные методы массивов — нельзя.

////////////////// Задание //////////////////
// const array = [1, 2, 3, 4];

////////////////// Решение //////////////////

const array = [1, 2, 3, 4];

let sum = null;

for (let i = 0; i < array.length; i++) {
    if (array[i] % 2 === 0) {
        sum += array[i];
    }
}

console.log(sum); 
